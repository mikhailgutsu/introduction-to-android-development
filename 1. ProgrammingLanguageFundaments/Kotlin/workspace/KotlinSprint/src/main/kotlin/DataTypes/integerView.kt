package DataTypes

fun integerView() {
    val myByte: Byte = 10
    val myShort: Short = 20
    val myInt: Int = 100
    val myLong: Long = 1000L
    val myFloat: Float = 10.5F
    val myDouble: Double = 99.99

    println(myByte)
    println(myShort)
    println(myInt)
    println(myLong)
    println(myFloat)
    println(myDouble)
}
